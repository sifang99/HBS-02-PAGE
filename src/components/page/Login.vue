<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">后台管理系统</div>
            <el-form :model="param" :rules="rules" ref="login" label-width="0px" class="ms-content">
                <el-form-item prop="num">
                    <el-input v-model="param.num" placeholder="账号">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="pwd">
                    <el-input
                        type="password"
                        placeholder="密码"
                        v-model="param.pwd"
                        @keyup.enter.native="submitForm()"
                    >
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitForm()">登录</el-button>
                </div>
                <p class="login-tips">Tips : 输入正确的账号和密码</p>
            </el-form>
        </div>
    </div>
</template>

<script>
export default {
    data: function() {
        return {
            param: {
                num: '',
                pwd: '',
            },
            rules: {
                num: [{ required: true, message: '请输入账号', trigger: 'blur' }],
                pwd: [{ required: true, message: '请输入密码', trigger: 'blur' }],
            },
        };
    },
    methods: {
        submitForm() {
            this.$refs.login.validate(valid => {
                if (valid) {

                    this.$axios.post('/workerLogin', this.param)
                    .then((response) => {
                        if(response.data.isLogin){
                            this.$message.success("登陆成功！")
                            this.$router.push('/dashboard');
                            localStorage.setItem("tokenId", response.data.tokenId)
                            this.$store.commit('AdminLogin')
                            sessionStorage.setItem("adminName", response.data.adminName),
                            sessionStorage.setItem("adminRole", response.data.adminRole)
                        }else{
                            this.$message(response.data.message)
                        }
                    }).catch((error) => {
                        this.$message("登陆失败！")
                    })
                } else {
                    this.$message.error('请输入账号和密码');
                    console.log('error submit!!');
                    return false;
                }
            });
        },
    },
};
</script>

